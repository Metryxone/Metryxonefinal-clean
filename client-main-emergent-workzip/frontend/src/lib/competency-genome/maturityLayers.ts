/**
 * Competency Maturity Layers
 * 5-level maturity model for each of the 24 competency domains.
 * Each level has a label, behavioural descriptor, and evidence signals.
 */

export interface MaturityLevel {
  level:       1 | 2 | 3 | 4 | 5;
  label:       string;
  descriptor:  string;
  evidenceSignals: string[];
}

export interface CompetencyMaturity {
  competencyId: string;
  levels: Record<1 | 2 | 3 | 4 | 5, MaturityLevel>;
}

const def = (
  competencyId: string,
  l1: [string, string, string[]],
  l2: [string, string, string[]],
  l3: [string, string, string[]],
  l4: [string, string, string[]],
  l5: [string, string, string[]],
): CompetencyMaturity => ({
  competencyId,
  levels: {
    1: { level: 1, label: l1[0], descriptor: l1[1], evidenceSignals: l1[2] },
    2: { level: 2, label: l2[0], descriptor: l2[1], evidenceSignals: l2[2] },
    3: { level: 3, label: l3[0], descriptor: l3[1], evidenceSignals: l3[2] },
    4: { level: 4, label: l4[0], descriptor: l4[1], evidenceSignals: l4[2] },
    5: { level: 5, label: l5[0], descriptor: l5[1], evidenceSignals: l5[2] },
  },
});

