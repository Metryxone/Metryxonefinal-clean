const MATURITY_MAP = new Map<string, CompetencyMaturity>(MATURITY_LAYERS.map(m => [m.competencyId, m]));

export function getMaturity(competencyId: string): CompetencyMaturity | undefined {
  return MATURITY_MAP.get(competencyId);
}

export function getLevelDescriptor(competencyId: string, level: number): MaturityLevel | null {
  const clamped = Math.max(1, Math.min(5, Math.round(level))) as 1 | 2 | 3 | 4 | 5;
  return MATURITY_MAP.get(competencyId)?.levels[clamped] ?? null;
}

export function getNextLevelDescriptor(competencyId: string, currentLevel: number): MaturityLevel | null {
  const next = Math.min(5, Math.floor(currentLevel) + 1) as 1 | 2 | 3 | 4 | 5;
  if (next <= currentLevel) return null;
  return MATURITY_MAP.get(competencyId)?.levels[next] ?? null;
}
